// Bit Manipulation uses bitwise operators - to perform operations directly on the individual bits (binary representations) of numbers.

// Bitwise Operators
// Bitwise AND - Returns 1 if both bits are one -> 5(101) & 1(001) - returns 1(001)
// Bitwise OR - Returns 1 if at least one bit is 1, otherwise 0 -> 5(101) | 3(011) - returns 7(111)
// Bitwise XOR - Returns 1 if the bits are different, otherwise 0 -> 5(101) ^ 1(001) - returns 4(100)
// Bitwise NOT - Inverts all the bits -> ~5(000 101) - returns -6(111 010)
// Left Shift - Shifts bits left by a specified number, pushing bits (zeroes) from right -> 5(101) << 1 - returns 10(1010)
// Signed Right Shift - Shifts bits right, pushing copies of the leftmost sign bit in from the left -> 5(101) >> 1 - returns 2(10)
// Zero-fill Right Shift - Shifts bits right, pushing zeroes in from the left, regardless of sign -> 5(101) >>> 1 - returns 2(10)

